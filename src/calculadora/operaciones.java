package calculadora;

class operaciones {
    protected static int sumar(int a, int b){
        return a + b;
    }

    protected static int restar(int a, int b){
        return a - b;
    }

    protected static int mult(int a, int b){
        return a * b;
    }

    protected static int div(int a, int b){
        return a / b;
    }
}
